print("==================================================")
print("📈 스마트 주식 분할매수(물타기) 시뮬레이터 V1.0")
print("==================================================\n")

# 1. 보유 종목 정보 입력받기

print("[1] 보유 종목 정보를 입력하세요.")
stock_name = input("▶ 종목명: ")
old_price = int(input("▶ 현재 매수 단가(원): "))
old_qty = int(input("▶ 현재 보유 수량(주): "))
print()

# 2. 추가 매수(물타기) 계획 입력받기
print("[2] 추가 매수(물타기) 계획을 입력하세요.")
new_price = int(input("▶ 추가 매수 단가(원): "))
new_qty = int(input("▶ 추가 매수 수량(주): "))
print()

# 3. 시뮬레이션 데이터 연산 및 처리

old_total = old_price * old_qty      # 기존 총 매수 금액
new_total = new_price * new_qty      # 추가 총 매수 금액

total_qty = old_qty + new_qty        # 최종 보유 주식 수량
total_amount = old_total + new_total # 최종 총 투자 금액

avg_price = total_amount / total_qty # 예상 평균 단가 계산

# 4. 시뮬레이션 결과 화면 출력 (f-string 포맷팅 활용)
print("==================================================")
print("📊 분할매수 시뮬레이션 결과")
print("==================================================")
print(f"종목명: {stock_name}")
print(f"기존 총 투자금: {old_total} 원 ({old_qty}주)")
print(f"추가 투자금: {new_total} 원 ({new_qty}주)")
print("--------------------------------------------------")
print(f"👉 최종 보유 수량: {total_qty} 주")
print(f"👉 최종 총 투자금: {total_amount} 원")

print(f"🎯 예상 평균 단가: {avg_price:.1f} 원")
print("==================================================")
